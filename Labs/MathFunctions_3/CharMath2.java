public class CharMath2 {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        System.out.println("a: " +a+ ", b: " +b);
        System.out.println("(int)a: " +(int)a+ ", (int)b: " +(int)b);
        System.out.println("(int)(a+b): " +(a+b));
        System.out.println("(char)(a+b):" +(char)(a+b));

    }
}
